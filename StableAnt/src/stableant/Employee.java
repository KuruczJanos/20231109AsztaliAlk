
package stableant;

public class Employee {
    //Változók létrehozása
    Integer id;
    String name;
    String city;
    Double salary;
//Konstruktor létrehozása
    public Employee(Integer id, String name, String city, Double salary) {
        this.id = id;
        this.name = name;
        this.city = city;
        this.salary = salary;
    }
}
